package co.work.testpilot.runtime

import kotlinx.serialization.Serializable

data class Element(val type: ElementType, val id: Int, val label: String, val value: String? = null, val selected: Boolean? = null) {
    override fun toString() = listOfNotNull("${type.name}, id: $id, label: $label", value?.let { "value: $it" }, selected?.let{ "selected" }).joinToString(", ")
}

@Serializable
enum class ElementType {
    Unknown,
    Any,
    Other,
    Application,
    Group,
    Window,
    Sheet,
    Drawer,
    Alert,
    Dialog,
    Button,
    RadioButton,
    RadioGroup,
    CheckBox,
    DisclosureTriangle,
    PopUpButton,
    ComboBox,
    MenuButton,
    ToolbarButton,
    Popover,
    Keyboard,
    Key,
    NavigationBar,
    TabBar,
    TabGroup,
    Toolbar,
    StatusBar,
    Table,
    TableRow,
    TableColumn,
    OutlineRow,
    Browser,
    CollectionView,
    Slider,
    PageIndicator,
    ProgressIndicator,
    ActivityIndicator,
    SegmentedControl,
    Picker,
    PickerWheel,
    Switch,
    Toggle,
    Link,
    Image,
    Icon,
    SearchField,
    ScrollView,
    ScrollBar,
    StaticText,
    TextField,
    SecureTextField,
    DatePicker,
    TextView,
    Menu,
    MenuItem,
    MenuBar,
    MenuBarItem,
    Map,
